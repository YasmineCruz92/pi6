    private void entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_entrarActionPerformed
        if( txNome.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo nome precisa ser preenchido: ");
        } else if(txEmail.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo telefone precisa ser preenchido: ");
        } else if(senha.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "O campo Email precisa ser preenchido: ");
        } else if(jComboBox1.getSelectedItem().equals("escolha:")) {

            JOptionPane.showMessageDialog(null, "escolha sua regi√£o: ");

        } else {
            // efetua o cadastro
            if (UsuarioDAO.cadastrar(txNome.getText(), txEmail.getText(),
                senha.getText(), jComboBox1.getSelectedItem().toString())) {

            BoasVindas form = new BoasVindas();
            form.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Falha ao efetuar cadastro");
        }
        }

    }//GEN-LAST:event_entrarActionPerformed

