    private void populaPlantas () {
        
        listaPlantas = CatalogoDAO.catalogoDados();
        List<String> listaNomePlanta = new ArrayList<>();
        
        listaPlantas.forEach((item) -> {
            listaNomePlanta.add(item.getNomePlanta());
        });
        
        String[] arrayPlantas = listaNomePlanta.toArray(new String[0]);
        selectPlantas.setModel(new javax.swing.DefaultComboBoxModel<>(arrayPlantas));
    }

