    private void selectPlantasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selectPlantasActionPerformed

        txDescricao.setText("");
        txTempoSol.setText("");
        txTempoAgua.setText("");
        txTempoAdubo.setText("");
        novaPlanta = new Planta();
        
        for(int i = 0; i < listaPlantas.size(); i++) {
            if(listaPlantas.get(i).getNomePlanta().equals(selectPlantas.getSelectedItem().toString())){
                txDescricao.setText(listaPlantas.get(i).getDescricao());
                txTempoSol.setText(listaPlantas.get(i).getTempoSol());
                txTempoAgua.setText(listaPlantas.get(i).getRega());
                txTempoAdubo.setText(listaPlantas.get(i).getTempoAdubo());

                novaPlanta = listaPlantas.get(i);
                return;
            }
        }
    }//GEN-LAST:event_selectPlantasActionPerformed

